let result;

function add(a,b) {
    return result = a + b;
}

function subtract(a,b) {
    return result = a - b;
}

function multiply(a,b) {
    return result = a * b;
}

function divide(a,b) {
    return result = a / b;
}

function increment(n) {
    return result = n + 1;
}

function decrement(n) {
    return result = n - 1;
}

function makeInt(n) {
    return result = parseInt(n,10);
}

function preserveDecimal(n) {
    return result = parseFloat(n);
}